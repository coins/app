// Shape Detection API Polyfill

class QrDecoder extends HTMLElement {

	connectedCallback() {
		const canvas = document.createElement('canvas');

		this.$canvas = canvas;
		this.appendChild(canvas);
	}

}

customElements.define('qr-decoder', QrDecoder);


